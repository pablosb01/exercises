function calculateAverageAge(persons) {
  let sumaEdades = 0;
  for(let i = 0; i < persons.length; i++){
    sumaEdades += persons[i].age;
  }
  let edadMedia = sumaEdades / persons.length;
  return edadMedia;
}

const persons = [
  { name: 'Paul', age: 16 },
  { name: 'George', age: 17 },
  { name: 'Lucas', age: 21 },
  { name: 'Marco', age: 32 },
  { name: 'Peter', age: 18 },
  { name: 'Carl', age: 13 },
  { name: 'Simon', age: 24 },
  { name: 'Mark', age: 15 },
  { name: 'Sandra', age: 34 },
  { name: 'Alice', age: 28 }
];

const average = calculateAverageAge(persons);
